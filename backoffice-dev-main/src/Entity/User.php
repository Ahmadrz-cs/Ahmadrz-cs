    /**
     * find the custom field and replaces it if different
     *
     * @param UserCustomFields $field
     * @param string $cf_key
     * @return bool
     *
     */
    public function findReplaceCustomField(UserCustomFields $field)
    {
        $found = false;
        /** @var UserCustomFields $cf */
        foreach ($this->customFields as $cf) {
            if ($cf->getFieldKey() == $field->getFieldKey()) {
                if ($cf->getFieldValue() != $field->getFieldValue()) {
                    $cf->setFieldValue($field->getFieldValue());
                }
                $found = true;
                break;
            }
        }

        if ($found == false) {
            $this->addCustomField($field);
        }

        return $found;
    }

