        // set the owning side of the relation if necessary
        if ($onboardingProfile !== null && $onboardingProfile->getUser() !== $this) {
            $onboardingProfile->setUser($this);
        }

        $this->onboardingProfile = $onboardingProfile;

        return $this;
    }

