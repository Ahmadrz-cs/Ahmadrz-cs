     */
    public function getKycReviewsNeedingAction(): Collection
    {
        $criteria = Criteria::create()
            ->where(Criteria::expr()->in('status', [
                KycReviewStatus::PendingSubjectAction->value,
            ]))
            ->setMaxResults(1);
